// imagePool reuses RGBA image buffers to avoid allocation per frame.
var imagePool = sync.Pool{
	New: func() interface{} {
		return &image.RGBA{}
	},
}

// jpegBufPool reuses bytes.Buffer for JPEG encoding.
var jpegBufPool = sync.Pool{
	New: func() interface{} {
		return &bytes.Buffer{}
	},
}

// Annotate draws bounding boxes and labels on an RGB frame and returns
// the annotated image as JPEG-encoded bytes.
//
// rgb must be packed RGB pixels (3 bytes per pixel, row-major) with
// dimensions width x height. The returned JPEG uses quality=75.
func Annotate(rgb []byte, width, height int, objects []AnnotateObject) ([]byte, error) {
	expected := width * height * 3
	if len(rgb) != expected {
		return nil, fmt.Errorf("annotate: expected %d RGB bytes (%dx%dx3), got %d", expected, width, height, len(rgb))
	}

	// Get or allocate an RGBA image buffer.
	img := imagePool.Get().(*image.RGBA)
	needAlloc := img.Rect.Dx() != width || img.Rect.Dy() != height
	if needAlloc {
		img = image.NewRGBA(image.Rect(0, 0, width, height))
	}
	defer imagePool.Put(img)

	// Convert RGB to RGBA.
	rgbToRGBA(rgb, img.Pix, width, height)

	// Draw each object.
	for _, obj := range objects {
		c := classPalette[obj.ClassID%len(classPalette)]
		x1 := clampInt(int(obj.X1), 0, width-1)
		y1 := clampInt(int(obj.Y1), 0, height-1)
		x2 := clampInt(int(obj.X2), 0, width-1)
		y2 := clampInt(int(obj.Y2), 0, height-1)

		// Draw rectangle (2px thick).
		drawRect(img, x1, y1, x2, y2, c, 2)

		// Draw label background and text above the box.
		label := obj.Label
		if label == "" {
			label = fmt.Sprintf("cls%d #%d %.0f%%", obj.ClassID, obj.TrackID, obj.Confidence*100)
		}
		drawLabel(img, x1, y1, label, c)
	}

	// Encode to JPEG.
	buf := jpegBufPool.Get().(*bytes.Buffer)
	buf.Reset()
	defer jpegBufPool.Put(buf)

	if err := jpeg.Encode(buf, img, &jpeg.Options{Quality: 75}); err != nil {
		return nil, fmt.Errorf("annotate: jpeg encode: %w", err)
	}

	// Copy the buffer so we can return it to the pool.
	out := make([]byte, buf.Len())
	copy(out, buf.Bytes())
	return out, nil
}

// rgbToRGBA converts packed RGB bytes to RGBA pixel data in-place.
// dst must have at least width*height*4 bytes capacity.
func rgbToRGBA(rgb []byte, dst []byte, width, height int) {
	nPixels := width * height
	for i := 0; i < nPixels; i++ {
		si := i * 3
		di := i * 4
		dst[di+0] = rgb[si+0]
		dst[di+1] = rgb[si+1]
		dst[di+2] = rgb[si+2]
		dst[di+3] = 255
	}
}

// drawRect draws a rectangle outline on img with the given thickness.
func drawRect(img *image.RGBA, x1, y1, x2, y2 int, c color.RGBA, thick int) {
	// Top and bottom horizontal lines.
	for t := 0; t < thick; t++ {
		// Top edge.
		ty := y1 + t
		if ty >= 0 && ty < img.Rect.Dy() {
			for x := x1; x <= x2; x++ {
				img.SetRGBA(x, ty, c)
			}
		}
		// Bottom edge.
		by := y2 - t
		if by >= 0 && by < img.Rect.Dy() {
			for x := x1; x <= x2; x++ {
				img.SetRGBA(x, by, c)
			}
		}
	}
	// Left and right vertical lines.
	for t := 0; t < thick; t++ {
		// Left edge.
		lx := x1 + t
		if lx >= 0 && lx < img.Rect.Dx() {
			for y := y1; y <= y2; y++ {
				img.SetRGBA(lx, y, c)
			}
		}
		// Right edge.
		rx := x2 - t
		if rx >= 0 && rx < img.Rect.Dx() {
			for y := y1; y <= y2; y++ {
				img.SetRGBA(rx, y, c)
			}
		}
	}
}

// drawLabel renders a text label above a bounding box using a simple
// bitmap approach. Each character is 6x10 pixels.
func drawLabel(img *image.RGBA, x, y int, label string, c color.RGBA) {
	charW, charH := 6, 10
	labelW := len(label) * charW
	labelH := charH + 2 // 1px padding top and bottom

	// Background rect above the box.
	bgY1 := y - labelH
	if bgY1 < 0 {
		bgY1 = 0
	}
	bgY2 := bgY1 + labelH
	bgX2 := x + labelW
	if bgX2 >= img.Rect.Dx() {
		bgX2 = img.Rect.Dx() - 1
	}

	// Fill background.
	bgColor := color.RGBA{R: c.R / 2, G: c.G / 2, B: c.B / 2, A: 200}
	for py := bgY1; py < bgY2; py++ {
		for px := x; px < bgX2; px++ {
			if px >= 0 && px < img.Rect.Dx() && py >= 0 && py < img.Rect.Dy() {
				img.SetRGBA(px, py, bgColor)
			}
		}
	}

	// Draw text using simple bitmap font.
	textY := bgY1 + 1
	textX := x + 1
	for _, ch := range label {
		drawChar(img, textX, textY, byte(ch), color.RGBA{R: 255, G: 255, B: 255, A: 255})
		textX += charW
	}
}

// drawChar renders a single ASCII character at (x, y) using a minimal 5x8 bitmap.
// Only printable ASCII is supported; unknown chars are rendered as a filled block.
func drawChar(img *image.RGBA, x, y int, ch byte, c color.RGBA) {
	glyph := getGlyph(ch)
	for row := 0; row < 8; row++ {
		bits := glyph[row]
		for col := 0; col < 5; col++ {
			if bits&(1<<(4-col)) != 0 {
				px := x + col
				py := y + row
				if px >= 0 && px < img.Rect.Dx() && py >= 0 && py < img.Rect.Dy() {
					img.SetRGBA(px, py, c)
				}
			}
		}
	}
}

// getGlyph returns the 5x8 bitmap for a printable ASCII character.
// Each byte encodes one row, with bit 4 being the leftmost pixel.
func getGlyph(ch byte) [8]byte {
	if ch >= 32 && ch < 128 {
		idx := int(ch - 32)
		if idx < len(font5x8) {
			return font5x8[idx]
		}
	}
	// Fallback: filled block.
	return [8]byte{0x1F, 0x1F, 0x1F, 0x1F, 0x1F, 0x1F, 0x1F, 0x1F}
}

