// CocoClassName returns the COCO class name for a given class ID (0-79).
// Returns "unknown" for out-of-range IDs.
func CocoClassName(classID int) string {
	if classID >= 0 && classID < len(cocoClassNames) {
		return cocoClassNames[classID]
	}
	return "unknown"
}

